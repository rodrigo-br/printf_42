#include "printf.h"

t_bool	ft_isspecifiers(char c)
{
	if (c == 'c'
		|| c == 's'
		|| c == 'p'
		|| c == 'd'
		|| c == 'i'
		|| c == 'f'
		|| c == 'u'
		|| c == 'x'
		|| c == 'X')
		return (TRUE);
	return (FALSE);
}
